package Demo;

public class Gender {
	public static void main (String arga[]) {
		char Gender='m';
		switch(Gender){
		case 'm':System.out.println("gender is male");
		break;
		case 'f':System.out.println("gender is female");
		break;
		default :System.out.println("unspecified gender");
		break;
			
		}
	}

}
