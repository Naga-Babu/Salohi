package Demo;

public class Grade {
	public static void main(String args[]) {
		char grade='c';
		switch(grade) {
		case 'a':System.out.println("Excellent");
		break;
		case 'b':System.out.println("very good");
		break;
		case 'c':System.out.println("good");
		break;
		case 'd':System.out.println("average");
		break;
		}
	}

}
