package Demo;

public class PosNeg {
	public static void main (String arga[]) {
		int num=-333;
		if(num>0) {
			System.out.println(num+" is positive number");
		}
		else if(num<0) {
			System.out.println(num+" is Negative number");
		}
		else {
			System.out.println(num+" is Zero number");
		}
	}

}
